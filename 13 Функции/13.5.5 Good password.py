def is_password_good(password):
    if len(password) < 8:
        return False
    for i in password:
        if i.isdigit():
            break
    else:
        return False
    for i in password:
        if i.isalpha() and i == i.upper():
            break
    else:
        return False
    for i in password:
        if i.isalpha() and i == i.lower():
            break
    else:
        return False
    return True


# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
txt = input()

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
print(is_password_good(txt))
